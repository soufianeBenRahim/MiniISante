    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
           try {
                   MetierImplimentationTiers M=new MetierImplimentationTiers();
                   Tiers T = new Tiers();
                   T.setNss(jTextFieldNSS.getText());
                   T.setNom(jTextFieldNom.getText());
                   T.setPrenom(jTextFieldPrinom.getText());
                   T.setAdress(jTextFieldAdrsse.getText());
                   T.setDatenaissance(jDatenaissance.getDate());
                   T.setAmois((Boolean)jCheckBoxAmois.isSelected());
            
            switch (ModeAppale) {
                case "Modifier":

                M.SavePatient(T);
                   
                    break;
                case "Ajouter":

                M.UpdatePatient(T);
                    break;
            }
        } catch (Exception ex) {
            Logger.getLogger(FicheTiers.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);

    }//GEN-LAST:event_jButtonOKActionPerformed

    

